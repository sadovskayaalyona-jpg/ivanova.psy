// Вопросы каждого типа идут вперемешку (а не подряд одним блоком),
// чтобы категория не угадывалась по группировке вопросов на экране.
export function getFlatQuestions() {
  const maxLen = Math.max(
    ...neurotype.dimensions.map((d) => d.questions.length)
  );
  const flat = [];
  for (let i = 0; i < maxLen; i++) {
    for (const dim of neurotype.dimensions) {
      if (dim.questions[i] !== undefined) {
        flat.push({
          dimKey: dim.key,
          text: dim.questions[i],
          fieldName: `${dim.key}__${i}`,
        });
      }
    }
  }
  return flat;
}
